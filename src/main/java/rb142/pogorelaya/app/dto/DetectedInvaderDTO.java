package rb142.pogorelaya.app.dto;

public class DetectedInvaderDTO {

    String name;
    int x;
    int y;

    public DetectedInvaderDTO() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}
